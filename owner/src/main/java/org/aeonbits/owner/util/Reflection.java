package org.aeonbits.owner.util;

/**
 * @author Luigi R. Viggiano
 */
public class Reflection {
    public static boolean isClassAvailable(String className) {
        try {
            Class.forName(className);
            return true;
        } catch (ClassNotFoundException e) {
            return false;
        }
    }
}
